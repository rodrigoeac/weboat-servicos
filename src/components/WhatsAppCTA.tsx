import type { Servico } from '../types/servico.ts';
import { WHATSAPP_NUMERO } from '../constants.ts';
import { formatCurrency } from '../utils/formatCurrency.ts';
import type { createT } from '../i18n.ts';
import { trackEvent } from '../utils/analytics.ts';

interface WhatsAppCTAProps {
  servicosSelecionados: Servico[];
  total: number;
  numConvidados: number;
  temConsultar: boolean;
  t: ReturnType<typeof createT>;
}

function buildWhatsAppMessage(
  servicos: Servico[],
  total: number,
  numConvidados: number,
  temConsultar: boolean,
  t: ReturnType<typeof createT>,
): string {
  if (servicos.length === 0) {
    return t('whatsapp.msgVazia');
  }

  const listaServicos = servicos.map((s) => `- ${s.nome}`).join('\n');
  const intro = t('whatsapp.msgIntro', { n: numConvidados });
  const totalLabel = `${t('whatsapp.totalEstimado')}: ${formatCurrency(total)}`;
  const consultar50 = temConsultar ? `\n\n${t('whatsapp.consultar50pes')}` : '';
  const maisInfo = t('whatsapp.maisInfo');

  return `${intro}\n\n${listaServicos}\n\n${totalLabel}${consultar50}\n\n${maisInfo}`;
}

export function WhatsAppCTA({
  servicosSelecionados,
  total,
  numConvidados,
  temConsultar,
  t,
}: WhatsAppCTAProps) {
  const msg = buildWhatsAppMessage(servicosSelecionados, total, numConvidados, temConsultar, t);
  const url = `https://wa.me/${WHATSAPP_NUMERO}?text=${encodeURIComponent(msg)}`;

  const handleClick = () => {
    trackEvent('whatsapp_click', {
      num_services: servicosSelecionados.length,
      total,
      num_guests: numConvidados,
      services: servicosSelecionados.map((s) => s.id).join(','),
    });
  };

  return (
    <a
      href={url}
      target="_blank"
      rel="noopener noreferrer"
      onClick={handleClick}
      className="
        flex items-center justify-center gap-2 w-full
        bg-whatsapp hover:bg-whatsapp-hover
        text-white font-heading font-semibold text-base
        py-3.5 px-6 rounded-xl
        transition-colors shadow-md
      "
    >
      <svg className="w-5 h-5" viewBox="0 0 24 24" fill="currentColor">
        <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z" />
      </svg>
      {t('whatsapp.falar')}
    </a>
  );
}
